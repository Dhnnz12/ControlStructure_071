a = int(input("Masukan Angka Pertama: "))# menampilkan pesan "Masukan Angka Pertama"
b = int(input("Masukan Angka Kedua: "))# menampilkan pesan "Masukan angka kedua"
c = int(input("Masukan Angka Ketiga: "))# menampilkan pesan "Masukan angka ketiga"

AngkaTerbesar = max(a, b, c) #Fungsi max untuk menentukan nilai terbesar di antara ketiga angka yang telah dimasukkan (a, b, dan c).

print("Angka Terbesar:",AngkaTerbesar) # digunakan untuk menampilkan string "Angka Terbesar" 